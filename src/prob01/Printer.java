package prob01;

public class Printer {

	public void println(int valueForPrint) {
		System.out.println(valueForPrint);
	}

	public void println(double valueForPrint) {
		System.out.println(valueForPrint);
	}

	public void println(boolean valueForPrint) {
		System.out.println(valueForPrint);
	}

	public void println(String valueForPrint) {
		System.out.println(valueForPrint);
	}

}
